using System.Text;

namespace RetroMultiTools.Utilities;

/// <summary>
/// Generates emulator configuration files for popular retro gaming emulators.
/// Supports RetroArch, Mesen, Snes9x, Project64, mGBA, Kega Fusion, Mednafen, Stella, FCEUX, and MAME.
/// </summary>
public static class EmulatorConfigGenerator
{
    public enum Emulator
    {
        RetroArch,
        Mesen,
        Snes9x,
        Project64,
        MGBA,
        KegaFusion,
        Mednafen,
        Stella,
        FCEUX,
        MAME
    }

    public static string GetEmulatorName(Emulator emulator) => emulator switch
    {
        Emulator.RetroArch => "RetroArch",
        Emulator.Mesen => "Mesen (NES/SNES)",
        Emulator.Snes9x => "Snes9x (SNES)",
        Emulator.Project64 => "Project64 (N64)",
        Emulator.MGBA => "mGBA (Game Boy/GBA)",
        Emulator.KegaFusion => "Kega Fusion (Sega)",
        Emulator.Mednafen => "Mednafen (Multi-system)",
        Emulator.Stella => "Stella (Atari 2600)",
        Emulator.FCEUX => "FCEUX (NES)",
        Emulator.MAME => "MAME (Arcade)",
        _ => emulator.ToString()
    };

    public static string GetConfigExtension(Emulator emulator) => emulator switch
    {
        Emulator.RetroArch => ".cfg",
        Emulator.Mesen => ".xml",
        Emulator.Snes9x => ".cfg",
        Emulator.Project64 => ".cfg",
        Emulator.MGBA => ".ini",
        Emulator.KegaFusion => ".ini",
        Emulator.Mednafen => ".cfg",
        Emulator.Stella => ".ini",
        Emulator.FCEUX => ".cfg",
        Emulator.MAME => ".ini",
        _ => ".cfg"
    };

    /// <summary>
    /// Generates a configuration file for the specified emulator.
    /// </summary>
    public static async Task GenerateAsync(
        EmulatorConfigOptions options,
        string outputPath,
        IProgress<string>? progress = null)
    {
        if (string.IsNullOrEmpty(outputPath))
            throw new ArgumentException("Output path cannot be empty.", nameof(outputPath));

        progress?.Report($"Generating {GetEmulatorName(options.Emulator)} configuration...");

        string config = options.Emulator switch
        {
            Emulator.RetroArch => GenerateRetroArch(options),
            Emulator.Mesen => GenerateMesen(options),
            Emulator.Snes9x => GenerateSnes9x(options),
            Emulator.Project64 => GenerateProject64(options),
            Emulator.MGBA => GenerateMgba(options),
            Emulator.KegaFusion => GenerateKegaFusion(options),
            Emulator.Mednafen => GenerateMednafen(options),
            Emulator.Stella => GenerateStella(options),
            Emulator.FCEUX => GenerateFceux(options),
            Emulator.MAME => GenerateMame(options),
            _ => throw new InvalidOperationException($"Unsupported emulator: {options.Emulator}")
        };

        try
        {
            string? dir = Path.GetDirectoryName(outputPath);
            if (!string.IsNullOrEmpty(dir))
                Directory.CreateDirectory(dir);

            await File.WriteAllTextAsync(outputPath, config).ConfigureAwait(false);
        }
        catch
        {
            try { File.Delete(outputPath); } catch (IOException) { } catch (UnauthorizedAccessException) { }
            throw;
        }

        progress?.Report("Done.");
    }

    private static string GenerateRetroArch(EmulatorConfigOptions options)
    {
        var sb = new StringBuilder();
        sb.AppendLine("# RetroArch Configuration");
        sb.AppendLine($"# Generated by RetroMultiTools on {DateTime.Now:yyyy-MM-dd HH:mm:ss}");
        sb.AppendLine();

        // Video settings
        sb.AppendLine("# Video");
        sb.AppendLine($"video_fullscreen = \"{options.Fullscreen.ToString().ToLowerInvariant()}\"");
        sb.AppendLine($"video_smooth = \"{options.SmoothVideo.ToString().ToLowerInvariant()}\"");
        sb.AppendLine($"video_shader_enable = \"{options.EnableShaders.ToString().ToLowerInvariant()}\"");
        sb.AppendLine($"video_vsync = \"{options.VSync.ToString().ToLowerInvariant()}\"");
        sb.AppendLine($"video_driver = \"{options.VideoDriver}\"");
        sb.AppendLine($"video_threaded = \"{options.ThreadedVideo.ToString().ToLowerInvariant()}\"");
        sb.AppendLine($"video_scale_integer = \"{options.IntegerScaling.ToString().ToLowerInvariant()}\"");
        if (!options.Fullscreen)
            sb.AppendLine("video_scale = \"3\""); // 3× native resolution for windowed mode
        sb.AppendLine("video_msg_pos_x = \"0.05\"");
        sb.AppendLine("video_msg_pos_y = \"0.05\"");

        sb.AppendLine();

        // Audio settings
        sb.AppendLine("# Audio");
        sb.AppendLine($"audio_enable = \"true\"");
        if (options.AudioDriver != "auto")
            sb.AppendLine($"audio_driver = \"{options.AudioDriver}\"");
        sb.AppendLine($"audio_sync = \"{options.AudioSyncRetroArch.ToString().ToLowerInvariant()}\"");
        // RetroArch uses a -80 to 0 dB scale; convert 0-100% linear volume to dB
        double retroArchVolumeDb = (options.AudioVolume * 80.0 / 100.0) - 80.0;
        sb.AppendLine($"audio_volume = \"{retroArchVolumeDb:F1}\"");
        sb.AppendLine("audio_latency = \"64\"");
        sb.AppendLine();

        // Input settings
        sb.AppendLine("# Input");
        if (options.InputDriver != "auto")
            sb.AppendLine($"input_driver = \"{options.InputDriver}\"");
        sb.AppendLine($"input_autodetect_enable = \"true\"");
        sb.AppendLine("input_menu_toggle_gamepad_combo = \"2\"");
        sb.AppendLine();

        // Paths
        sb.AppendLine("# Paths");
        if (!string.IsNullOrEmpty(options.RomDirectory))
        {
            string romDir = NormalizePath(options.RomDirectory);
            sb.AppendLine($"rgui_browser_directory = \"{romDir}\"");
            sb.AppendLine($"content_directory = \"{romDir}\"");
        }

        if (!string.IsNullOrEmpty(options.SaveDirectory))
            sb.AppendLine($"savefile_directory = \"{NormalizePath(options.SaveDirectory)}\"");

        if (!string.IsNullOrEmpty(options.StateDirectory))
            sb.AppendLine($"savestate_directory = \"{NormalizePath(options.StateDirectory)}\"");

        if (!string.IsNullOrEmpty(options.ScreenshotDirectory))
            sb.AppendLine($"screenshot_directory = \"{NormalizePath(options.ScreenshotDirectory)}\"");

        sb.AppendLine();

        // General / core settings
        sb.AppendLine("# General");
        sb.AppendLine($"rewind_enable = \"{options.EnableRewind.ToString().ToLowerInvariant()}\"");
        if (options.EnableRewind)
            sb.AppendLine("rewind_granularity = \"2\"");
        sb.AppendLine($"savestate_auto_save = \"{options.AutoSaveState.ToString().ToLowerInvariant()}\"");
        sb.AppendLine($"savestate_auto_load = \"{options.AutoSaveState.ToString().ToLowerInvariant()}\"");
        sb.AppendLine($"menu_driver = \"{options.MenuDriver}\"");
        sb.AppendLine($"fps_show = \"{options.ShowFPS.ToString().ToLowerInvariant()}\"");
        sb.AppendLine($"notification_show_when_menu_is_alive = \"{options.EnableNotifications.ToString().ToLowerInvariant()}\"");
        sb.AppendLine($"cheats_enable = \"{options.EnableCheats.ToString().ToLowerInvariant()}\"");
        sb.AppendLine($"config_save_on_exit = \"{options.ConfigSaveOnExit.ToString().ToLowerInvariant()}\"");
        sb.AppendLine($"pause_nonactive = \"{options.PauseOnUnfocus.ToString().ToLowerInvariant()}\"");

        // Latency reduction
        sb.AppendLine();
        sb.AppendLine("# Latency");
        sb.AppendLine($"run_ahead_enabled = \"{(options.RunAheadFrames > 0).ToString().ToLowerInvariant()}\"");
        sb.AppendLine($"run_ahead_frames = \"{options.RunAheadFrames}\"");

        return sb.ToString();
    }

