        sb.AppendLine("ctrlrpath                 ctrlr");
        sb.AppendLine("inipath                   .;ini;ini/presets");
        sb.AppendLine("fontpath                  .");
        sb.AppendLine("cheatpath                 cheat");
        sb.AppendLine("crosshairpath             crosshair");
        sb.AppendLine("pluginspath               plugins");
        sb.AppendLine("languagepath              language");
        sb.AppendLine();

        // Core output directory options
        sb.AppendLine("#");
        sb.AppendLine("# CORE OUTPUT DIRECTORY OPTIONS");
        sb.AppendLine("#");
        sb.AppendLine("cfg_directory             cfg");
        if (!string.IsNullOrEmpty(options.SaveDirectory))
            sb.AppendLine($"nvram_directory           {options.SaveDirectory}");
        else
            sb.AppendLine("nvram_directory           nvram");
        sb.AppendLine("input_directory           inp");
        if (!string.IsNullOrEmpty(options.StateDirectory))
            sb.AppendLine($"state_directory           {options.StateDirectory}");
        else
            sb.AppendLine("state_directory           sta");
        if (!string.IsNullOrEmpty(options.ScreenshotDirectory))
            sb.AppendLine($"snapshot_directory        {options.ScreenshotDirectory}");
        else
            sb.AppendLine("snapshot_directory        snap");
        sb.AppendLine("diff_directory            diff");
        sb.AppendLine("comment_directory         comments");
        sb.AppendLine();

        // Core performance options
        sb.AppendLine("#");
        sb.AppendLine("# CORE PERFORMANCE OPTIONS");
        sb.AppendLine("#");
        sb.AppendLine("autoframeskip             0");
        sb.AppendLine("frameskip                 0");
        sb.AppendLine("throttle                  1");
        sb.AppendLine("sleep                     1");
        sb.AppendLine("speed                     1.0");
        sb.AppendLine("refreshspeed              0");
        sb.AppendLine("lowlatency                0");
        sb.AppendLine();

        // Core rotation options
        sb.AppendLine("#");
        sb.AppendLine("# CORE ROTATION OPTIONS");
        sb.AppendLine("#");
        sb.AppendLine("rotate                    1");
        sb.AppendLine("ror                       0");
        sb.AppendLine("rol                       0");
        sb.AppendLine("autoror                   0");
        sb.AppendLine("autorol                   0");
        sb.AppendLine("flipx                     0");
        sb.AppendLine("flipy                     0");
        sb.AppendLine();

        // Core video options
        sb.AppendLine("#");
        sb.AppendLine("# CORE VIDEO OPTIONS");
        sb.AppendLine("#");
        sb.AppendLine("video                     auto");
        sb.AppendLine("numscreens                1");
        sb.AppendLine($"window                    {(options.Fullscreen ? "0" : "1")}");
        sb.AppendLine("maximize                  1");
        sb.AppendLine($"waitvsync                 {BoolToInt(options.VSync)}");
        sb.AppendLine("syncrefresh               0");
        if (options.IntegerScaling)
            sb.AppendLine("unevenstretch             0");
        if (options.SmoothVideo)
            sb.AppendLine("filter                    1");
        else
            sb.AppendLine("filter                    0");
        sb.AppendLine();

        // Core sound options
        sb.AppendLine("#");
        sb.AppendLine("# CORE SOUND OPTIONS");
        sb.AppendLine("#");
        sb.AppendLine("sound                     1");
        sb.AppendLine("samplerate                48000");
        sb.AppendLine("samples                   1");
        // MAME uses a -32 to 0 dB scale; convert 0-100% linear volume to dB
        int mameVolumeDb = (options.AudioVolume * 32 / 100) - 32;
        sb.AppendLine($"volume                    {mameVolumeDb}");
        sb.AppendLine();

        // Core input options
        sb.AppendLine("#");
        sb.AppendLine("# CORE INPUT OPTIONS");
        sb.AppendLine("#");
        sb.AppendLine("coin_lockout              1");
        sb.AppendLine("mouse                     0");
        sb.AppendLine("joystick                  1");
        sb.AppendLine("lightgun                  0");
        sb.AppendLine("multikeyboard             0");
        sb.AppendLine("multimouse                0");
        sb.AppendLine("steadykey                 0");
        sb.AppendLine("offscreen_reload          0");
        sb.AppendLine("natural                   0");
        sb.AppendLine();

        // Core state/playback options
        sb.AppendLine("#");
        sb.AppendLine("# CORE STATE/PLAYBACK OPTIONS");
        sb.AppendLine("#");
        sb.AppendLine($"autosave                  {BoolToInt(options.AutoSaveState)}");
        sb.AppendLine($"rewind                    {BoolToInt(options.EnableRewind)}");
        sb.AppendLine("rewind_capacity           100");
        sb.AppendLine();

        // OSD options
        sb.AppendLine("#");
        sb.AppendLine("# OSD OPTIONS");
        sb.AppendLine("#");
        if (options.EnableShaders)
        {
            sb.AppendLine("gl_glsl                   1");
            sb.AppendLine("gl_glsl_filter            1");
        }
        sb.AppendLine($"skip_gameinfo             {BoolToInt(options.SkipGameInfo)}");
        sb.AppendLine($"skip_warnings             {BoolToInt(options.SkipWarnings)}");
        sb.AppendLine($"cheat                     {BoolToInt(options.EnableCheats)}");

        return sb.ToString();
    }

    private static string BoolToInt(bool value) => value ? "1" : "0";

    /// <summary>
    /// Normalizes a file path for cross-platform emulator configs by replacing
    /// backslashes with forward slashes.
    /// </summary>
    private static string NormalizePath(string path) => path.Replace("\\", "/");

    private static string EscapeXml(string value) =>
        value.Replace("&", "&amp;").Replace("<", "&lt;").Replace(">", "&gt;")
             .Replace("\"", "&quot;").Replace("'", "&apos;");
}

