/// <summary>
/// Generates custom 1bpp/2bpp fonts for the Analogue Mega SG, NT, and Super NT
/// menu systems. These consoles use 8×8 pixel character tiles stored in a packed
/// binary format.
/// </summary>
public static class AnalogueFontGenerator
{
    /// <summary>
    /// Supported Analogue console targets for font generation.
    /// </summary>
    public enum ConsoleTarget
    {
        MegaSg,
        Nt,
        SuperNt
    }

    /// <summary>
    /// The font file layout: 256 characters × 8 rows × 1 byte per row = 2048 bytes.
    /// Each byte stores 8 horizontal pixels (MSB = left, LSB = right).
    /// </summary>
    private const int CharCount = 256;
    private const int CharHeight = 8;
    private const int CharWidth = 8;
    private const int FontFileSize = CharCount * CharHeight; // 2048 bytes

    /// <summary>
    /// A minimal built-in 8×8 ASCII font (printable range 0x20–0x7E).
    /// Each character is 8 bytes (one per row, MSB-left).
    /// Only covers basic ASCII; remaining slots are zeroed.
    /// </summary>
    private static readonly Dictionary<char, byte[]> BuiltInFont = BuildMinimalFont();

    /// <summary>
    /// Generates a font binary file for the specified console target using the
    /// built-in minimal font. Characters not in the built-in set are left blank.
    /// </summary>
    public static async Task GenerateDefaultFontAsync(
        string outputPath,
        ConsoleTarget target,
        IProgress<string>? progress = null)
    {
        progress?.Report($"Generating default font for {target}...");

        byte[] fontData = new byte[FontFileSize];

        // Fill in the built-in font data
        foreach (var (ch, rows) in BuiltInFont)
        {
            int charIndex = (int)ch;
            if (charIndex >= CharCount) continue;

            int offset = charIndex * CharHeight;
            for (int row = 0; row < CharHeight && row < rows.Length; row++)
                fontData[offset + row] = rows[row];
        }

        progress?.Report("Writing font file...");
        string? dir = Path.GetDirectoryName(outputPath);
        if (dir != null)
            Directory.CreateDirectory(dir);

        await File.WriteAllBytesAsync(outputPath, fontData).ConfigureAwait(false);
        progress?.Report("Font generated successfully.");
    }

    /// <summary>
    /// Generates a font binary file from a source image file.
    /// The image must be a 128×128 pixel BMP containing a 16×16 grid of 8×8 characters.
    /// </summary>
    public static async Task GenerateFontFromImageAsync(
        string imagePath,
        string outputPath,
        ConsoleTarget target,
        IProgress<string>? progress = null)
    {
        if (!File.Exists(imagePath))
            throw new FileNotFoundException("Font source image not found.", imagePath);

        progress?.Report("Reading source image...");
        byte[] imageData = await File.ReadAllBytesAsync(imagePath).ConfigureAwait(false);

        await Task.Run(() =>
        {
            // Parse BMP header to extract pixel data
            if (imageData.Length < 54 || imageData[0] != 'B' || imageData[1] != 'M')
                throw new InvalidOperationException("Invalid BMP file format.");

            int pixelOffset = BitConverter.ToInt32(imageData, 10);
            int width = BitConverter.ToInt32(imageData, 18);
            int height = Math.Abs(BitConverter.ToInt32(imageData, 22));
            int bpp = BitConverter.ToInt16(imageData, 28);
            bool topDown = BitConverter.ToInt32(imageData, 22) < 0;

            if (width != 128 || height != 128)
                throw new InvalidOperationException(
                    $"Font image must be 128×128 pixels (got {width}×{height}). " +
                    "The image should contain a 16×16 grid of 8×8 characters.");

            if (bpp != 24 && bpp != 32)
                throw new InvalidOperationException(
                    $"Font image must be 24-bit or 32-bit BMP (got {bpp}-bit).");

            int bytesPerPixel = bpp / 8;
            int rowStride = (width * bytesPerPixel + 3) & ~3;
            byte[] fontData = new byte[FontFileSize];

            for (int charY = 0; charY < 16; charY++)
            {
                for (int charX = 0; charX < 16; charX++)
                {
                    int charIndex = charY * 16 + charX;
                    int fontOffset = charIndex * CharHeight;

                    for (int row = 0; row < CharHeight; row++)
                    {
                        byte rowBits = 0;
                        for (int col = 0; col < CharWidth; col++)
                        {
                            int imgX = charX * 8 + col;
                            int imgY = charY * 8 + row;

                            // BMP is bottom-up unless height is negative
                            int bmpRow = topDown ? imgY : (height - 1 - imgY);
                            int pixelIndex = pixelOffset + bmpRow * rowStride + imgX * bytesPerPixel;

                            if (pixelIndex + 2 < imageData.Length)
                            {
                                // BGR order in BMP — compute luminance
                                int b = imageData[pixelIndex];
                                int g = imageData[pixelIndex + 1];
                                int r = imageData[pixelIndex + 2];
                                int lum = (r * 299 + g * 587 + b * 114) / 1000;

                                // Threshold: dark pixels = set bit
                                if (lum < 128)
                                    rowBits |= (byte)(0x80 >> col);
                            }
                        }

                        fontData[fontOffset + row] = rowBits;
                    }
                }
            }

            progress?.Report("Writing font file...");
            string? dir = Path.GetDirectoryName(outputPath);
            if (dir != null)
                Directory.CreateDirectory(dir);

            File.WriteAllBytes(outputPath, fontData);
        }).ConfigureAwait(false);

        progress?.Report("Font generated successfully.");
    }

    /// <summary>
    /// Returns the expected font filename for the given console target.
    /// </summary>
    public static string GetDefaultFontFileName(ConsoleTarget target) => target switch
    {
        ConsoleTarget.MegaSg => "font.bin",
        ConsoleTarget.Nt => "font.bin",
        ConsoleTarget.SuperNt => "font.bin",
        _ => "font.bin"
    };

    /// <summary>
    /// Returns the expected font directory relative to the SD card root.
    /// </summary>
    public static string GetFontDirectory(ConsoleTarget target) => target switch
    {
        ConsoleTarget.MegaSg => "System",
        ConsoleTarget.Nt => "System",
        ConsoleTarget.SuperNt => "System",
        _ => "System"
    };

